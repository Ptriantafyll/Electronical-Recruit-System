    private void showjobsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showjobsButtonActionPerformed
        try
        {
            int height = jobsList.getComponentCount();
            for(int i=0;i<height;i++)
            {
                jobsList.remove(0);
            }
            DefaultListModel<String> tempjobsList = new DefaultListModel<>();
            
            String firmsQuery="select position from job";            
            Statement stmt1 = conn.createStatement();
            ResultSet rs1 = stmt1.executeQuery(firmsQuery);
            
            while(rs1.next())
            {
                tempjobsList.addElement(rs1.getString("position"));
            }
            
            jobsList.setModel(tempjobsList);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_showjobsButtonActionPerformed

