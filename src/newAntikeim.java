    private void addNewAntikeimenoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewAntikeimenoButtonActionPerformed
        String newtitle = newAntikeimTable.getValueAt(0, 0).toString();
        try // add antikeim
        {   
            String insertQuery;
            String descr = newAntikeimTable.getValueAt(0, 1).toString();
            String belongsto = belongsToComboBox.getSelectedItem().toString();
            
            
            if(belongsto.equals("NULL"))
                 insertQuery = "insert into antikeim (title, descr, belongs_to) values "
                        + "( '" + newtitle +"' , '" +descr + "' , " +belongsto + " )";
            else    
                insertQuery = "insert into antikeim (title, descr, belongs_to) values "
                        + "( '" + newtitle +"' , '" +descr + "' , '" +belongsto + "' )";
            
            JOptionPane.showMessageDialog(null, insertQuery);
            
            pst =conn.prepareStatement(insertQuery);
            pst.execute(); 
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        try// add into required
        {
            String job="";
            int id =-1;
            java.util.List<String> jobs = jobsList.getSelectedValuesList();
            
            for(int i=0; i<jobs.size(); i++)
            {
                job = jobs.get(i);
                String idQuery = "select id,position from job";
                
                Statement stmt1 = conn.createStatement();
                ResultSet rs1 = stmt1.executeQuery(idQuery);
                while(rs1.next())
                {
                    if(rs1.getString("position").equals(job))
                    {
                        id = rs1.getInt("id");
                    }
                }
                
                String hastomeasQuery="insert into requires (job_id, antikeim_title) values"
                    + "( " +id +" , '" +newtitle +"' )";
            
                JOptionPane.showMessageDialog(null, hastomeasQuery);

                pst =conn.prepareStatement(hastomeasQuery);
                pst.execute();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addNewAntikeimenoButtonActionPerformed

